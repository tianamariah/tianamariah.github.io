function toggle() {
    var x = document.getElementById("navbarNavAltMarkup");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function showGalleryMenu() {
    var x = document.getElementById("galleryNav");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}


